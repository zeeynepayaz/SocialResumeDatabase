    CREATOR_HAS_PROFILE.ProfileId = CREATOR_PROFILE.ProfileId
    AND SOCIAL_RESUME_USER.CreatorId = CREATOR_HAS_PROFILE.CreatorId;

    INSERT INTO CONNECTION_LIST
    VALUES(fromUserCreatorId,toUserCreatorId,TO_DATE(SYSDATE,'DD/MM/YYYY'),:NEW.ActionId);
  END IF;
END;

